# Solicite dois números e informe as operações matemáticas com eles

x = int(input("Digite o primeiro valor: "))
y = int(input("Digite o segundo valor: "))

vAdd = x + y
vSubt = x - y
vMult = x * y
vDiv = x / y

print("Seguem as operações matemáticas envolvendo os dois números: ")
print("Adição: ", vAdd)
print("Substração", vSubt)
print("Multiplicação: ", vMult)
print("Divisão: ", vDiv)
